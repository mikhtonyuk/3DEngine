#include "SvgGroup.h"
#include "SvgStyle.h"
#include "SvgVisitor.h"

#include <cassert>

namespace Engine
{
	namespace UI
	{
		namespace SVG
		{
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
			void SvgGroup::SetParent(SvgObject* parent)
			{
				m_parent = parent;
			}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
			SvgObject* SvgGroup::Parent() const
			{
				return m_parent;
			}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
			void SvgGroup::Accept(SvgVisitor* visitor)
			{
				if(!visitor->VisitEnter(this))
					return;

				for(std::vector<SvgObject*>::const_iterator i = Children().begin(); i != Children().end(); i++)
				{
					(*i)->Accept(visitor);
				}

				visitor->VisitExit(this);
			}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		}
	}
}

