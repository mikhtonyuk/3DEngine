#include "SvgStyle.h"


namespace Engine
{
	namespace UI
	{
		namespace SVG
		{
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
			SvgStyle::SvgStyle()
				:m_opacity(1), m_fillStyle(FS_SOLID), m_strokeStyle(SS_LINE_SOLID)
			{
				m_fillColor.Red		= 0;
				m_fillColor.Green	= 0;
				m_fillColor.Blue	= 0; 
				
				m_strokeColor.Red	= 0;
				m_strokeColor.Green = 0;
				m_strokeColor.Blue	= 0; 

				m_strokeWidth		= 1;
			}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
			const Color& SvgStyle::FillColor() const
			{
				if (FillStyle() == FS_NONE)
					throw SvgException("There is no fill");

				return m_fillColor;
			}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
			const Color& SvgStyle::StrokeColor() const
			{
				if (StrokeStyle() == SS_NONE)
					throw SvgException("There is no stroke");

				return m_strokeColor;
			}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
			double SvgStyle::StrokeWidth() const
			{
				return m_strokeWidth;
			}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
			double SvgStyle::Opacity() const
			{
				return m_opacity;
			}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
			SVG_FILL_STYLE SvgStyle::FillStyle() const
			{
				return m_fillStyle;
			}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
			void SvgStyle::SetFillColor(const Color& color)
			{
				m_fillColor = color;
			}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
			SVG_STROKE_STYLE SvgStyle::StrokeStyle() const
			{
				return m_strokeStyle;
			}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
			void SvgStyle::SetStrokeColor(const Color& color)
			{
				m_strokeColor = color;
			}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
			void SvgStyle::SetStrokeWidth(double width)
			{
				m_strokeWidth = width;
			}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
			void SvgStyle::SetOpacity(double opacity)
			{
				m_opacity = opacity;
			}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
			void SvgStyle::SetFillStyle(SVG_FILL_STYLE style)
			{
				m_fillStyle = style;
			}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
			void SvgStyle::SetStrokeStyle(SVG_STROKE_STYLE style)
			{
				m_strokeStyle = style;
			}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		}
	}
}
